# EXEMPLO 1:
name = "Allison"

case(name)
  when "Brian"
    puts "É o Brian"
  when "Allison"
    puts "Não é o Brian"
  else
    puts "Não sei quem é"
end

# COMANDOS USADOS

  # case é uma condicional. Uma variável é passada como seu parâmetro. When é o caso possível e else passa o contrário de um when.
